package lesson2;

public class DefaultValuesOfPrimitiveDataType {
    static byte b;
    static short s;
    static int i;
    static  long l;
    static float f;
    static double d;
    static char c;
    static boolean boo;

    public static void main(String[] args) {
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println("=============");
        System.out.println(f);
        System.out.println(d);
        System.out.println("============");
        System.out.println(c);
        int charInt = c;
        System.out.println(charInt);
        System.out.println("==========");
        System.out.println(boo);
    }
}
