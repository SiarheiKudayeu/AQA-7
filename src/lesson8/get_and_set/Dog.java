package lesson8.get_and_set;

public class Dog {
    public String name;
    public int countOfLegs;

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public int getCountOfLegs(){
        return countOfLegs;
    }

    public void setCountOfLegs(int countOfLegs){
        this.countOfLegs = countOfLegs;
    }


}
